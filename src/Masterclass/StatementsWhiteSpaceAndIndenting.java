package Masterclass;

public class StatementsWhiteSpaceAndIndenting {
    public static void main(String[]args){
        int myVariable = 50; // this whole line is the statement.
        myVariable++; // Also a statement.
        myVariable--;
        //method calls are also a complete java statement
        //the semi colon is need to finish the statement
        //statements can be on multiple lines

        System.out.println("this is a test");

        System.out.println("this is"+
                " another test" +
                " and still more");

        //white space is the space between things to make it more readable.
        // Java ignores the white space.

//        indentation: each code block in indented to make it easier to read.
//        if you mess up the indenting go to code and Reformat Code (Ctrl + Alt + L)

    }
}
